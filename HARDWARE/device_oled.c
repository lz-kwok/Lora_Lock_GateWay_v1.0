/*输出汉字和字符混合字符串
*Y轴是按8格递进的，y轴0~63，只能按8格的倍数显示，
*因为列行式只能按8个字节进行
*
*/
void LCD_Print(uint8_t x, uint8_t y, uint8_t *ch)
{
    uint8_t ch2[3];
    uint8_t kk=0;        
    while(*(ch+kk) != '\0')
    {
        if(*(ch+kk) > 127)                      //大于127为中文，小于等于127为ASCII
        {
            ch2[0] = *(ch+kk);
            ch2[1] = *(ch+kk+1);
            ch2[2] = '\0';			//汉字为两个字节
            
            PutHanzi(x,y,ch2);	                //显示汉字
            x += 16;
            kk += 2;
        }
        else
        {
            ch2[0] = *(ch+kk);	
            ch2[1] = '\0';			//字母占一个字节

            PutAscii(x,y,ch2);	//显示字母
            x += 8;
            kk += 1;
        }
    }
} 

