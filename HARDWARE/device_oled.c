void LCD_P14x16Str(uint8_t x,uint8_t y,uint8_t *ch,uint8_t mode)
{
    uint8_t wm=0,ii = 0;
    uint16_t adder=1; 
    
    while(*(ch+ii) != '\0')
    {
    wm = 0;
    adder = 1;
    while(*(F14x16_Idx+wm) > 127)
    {
            if(*(F14x16_Idx+wm) == *(ch+ii))
            {
                    if(*(F14x16_Idx+wm + 1) == *(ch+ii + 1))
                    {
                            adder = wm * 14;
                            break;
                    }
            }
            wm += 2;			
    }
    if(x>118){x=0;y++;}
    LCD_Set_Pos(x , y); 
    if(adder != 1)// 显示汉字					
    {
            LCD_Set_Pos(x , y);
            for(wm = 0;wm < 14;wm++)               
            {
                LCD_WrDat(*(F14x16+adder),mode);	
      
                adder += 1;
            }      
            LCD_Set_Pos(x,y + 8); 
            for(wm = 0;wm < 14;wm++)          
            {
                LCD_WrDat(*(F14x16+adder),mode);
                    
                adder += 1;
            }   		
    }
    else			  //显示空白字符			
    {
            ii += 1;
            LCD_Set_Pos(x,y);
            for(wm = 0;wm < 14;wm++)
            {
                LCD_WrDat(0,mode);
            }
            LCD_Set_Pos(x,y + 1);
            for(wm = 0;wm < 14;wm++)
            {   		
                LCD_WrDat(0,mode);
            }
    }
    x += 14;
    ii += 2;
    }
}

