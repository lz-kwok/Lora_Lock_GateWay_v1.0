void LCD_WrDat(uint8_t data,uint8_t mode)
{
    uint8_t LcdData;
    
    LCD_DC_SET();
    
    if(mode)
      LcdData = data;
    else
      LcdData = ~data;
    System.Device.Spi1.WriteReadData(LcdData);
}

void LCD_WrCmd(uint8_t cmd)
{
    LCD_DC_CLR();
    
    System.Device.Spi1.WriteReadData(cmd);
}


/*设置起始地址
*Y轴是按8格递进的，y轴0~63，只能按8格的倍数显示，
*因为列行式只能按8个字节进行
*/
void LCD_Set_Pos(uint8_t x, uint8_t y)
{ 
    LCD_WrCmd(0xb0+(y>>3));
    LCD_WrCmd(((x&0xf0)>>4)|0x10);
    LCD_WrCmd((x&0x0f)|0x01); 
} 
void LCD_Fill(uint8_t bmp_data)
{
    uint8_t y,x;
    
    for(y=0;y<8;y++)
    {
        LCD_WrCmd(0xb0+y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10);
        
        for(x=0;x<X_WIDTH;x++)
          LCD_WrDat(bmp_data,1);
    }
}

void LCD_CLS(void)
{
    uint8_t y,x;	
    for(y=0;y<8;y++)
    {
        LCD_WrCmd(0xb0+y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10); 
        
        for(x=0;x<X_WIDTH;x++)
          LCD_WrDat(0,1);
    }
}
